#include "ft_strings.h"

int	ft_ffs(int i)
{
	int	bit;

	if (i == 0)
		return (0);
	bit = 1;
	while ((i & 1) == 0)
	{
		i >>= 1;
		bit++;
	}
	return (bit);
}
