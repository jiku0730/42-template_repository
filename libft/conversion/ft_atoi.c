#include "libft.h"

int	ft_atoi(const char *str)
{
	int	sign;
	int	num;

	while (ft_isspace(*str))
		str++;
	sign = 1;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			sign *= -1;
		str++;
	}
	num = 0;
	while (ft_isdigit((int)*str))
		num = 10 * num + (*str++ - '0');
	return (sign * num);
}
